<article class="oneColumns">
	<h1>Who</h1>
	<p>The Knights of Columbus is the world's largest Catholic family fraternal service organization with 1.6 million members. It provides members and their families with volunteer opportunities in service to the Catholic Church, their communities, families and young people.</p>
</article>
<article class="oneColumns">
	<h1>Benefits</h1>
	<p>As a member of the Knights of Columbus you and your family enjoy many benefits, including 12 free issues annually of the Columbia magazine, the world's largest Catholic family magazine, eligibility to join the Knights of Columbus top-ranked life insurance program, and many more family and personal benefits.</p>
	<p>There are many benefits about being a member of your loal council: Church Activities, Youth Activities, Family Activities, Community Service and Council Activities.</p>
</article>
<article class="oneColumns endColumns">
	<h1>How</h1>
	<p>Membership in the Knights of Columbus is open to practicing Catholic men in union with the Holy See, who are at least 18 years old. A practicing Catholic is one who lives up to the Commandments of God and the precepts of the Church. Application blanks are available from any member of the Knights of Columbus or from the foyer information shelves.</p>
</article>
